    public void tableDetails(){
        Bill bill = new Bill();
        
        String returncode = txtOtp.getText();
        bill = Billdao.getToken(dbid, returncode);
        dbToken = bill.getReturnCode();
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        ArrayList<Bill> list = Billdao.getAllRecordsByReturnCode(returncode);
        Iterator<Bill> itr = list.iterator();
        while(itr.hasNext()){
            Bill filmObj = itr.next();
            dtm.addRow(new Object[]{filmObj.getId(),filmObj.getGenre(),filmObj.getTitle(),filmObj.getRentdate(),filmObj.getTotal()});
           
        }
    }
   
