    private void btnApplyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnApplyActionPerformed
        // TODO add your handling code here:
  
        Bill bill = new Bill();
        bill = Billdao.getToken(dbid, txtOtp.getText());
        
        if(bill == null){
            JOptionPane.showMessageDialog(null,"<html><b style =\"color:red\">Return Code does not exist</b></html>","Message", JOptionPane.ERROR_MESSAGE);
        }else{
          
        
        
  
           
            
            String returncode = txtOtp.getText();
            dbToken = bill.getReturnCode();
            Boolean returnfilm = false;
            Boolean dbType = bill.getReturnFilm();
            
            if(returncode.equals(dbToken)&& returnfilm.equals(dbType)){
                btnApply.setEnabled(false);
                btnReturn.setEnabled(true);
                JOptionPane.showMessageDialog(null,"Return Code Exist","Message", JOptionPane.INFORMATION_MESSAGE);
                Date todaydate = new Date();
                SimpleDateFormat dFormat = new SimpleDateFormat("dd MMM yyyy");
                String date = dFormat.format(todaydate);
                txtTodaydate.setText(date);
                txtduedate.setText(bill.getDuedate());
                int cost = bill.getCost();   
                
                
                if (date.compareTo(bill.getDuedate())>0) {
                    JOptionPane.showMessageDialog(null,"Rent overdue P200 added to total cost","Message", JOptionPane.ERROR_MESSAGE);
                    int addcost = 200;
                    cost += addcost; 
                    Billdao.updateCost(dbToken, cost);
                    //bill.setCost(cost);
              
                } 
                String s = String.valueOf(cost);
                txtCost.setText(s);
                
                tableDetails();
            }
            else if(returncode.equals(dbToken)&& dbType.equals(true)){
                JOptionPane.showMessageDialog(null,"Return Code Already Used","Message", JOptionPane.ERROR_MESSAGE);
            }
        }
              
        
    }//GEN-LAST:event_btnApplyActionPerformed

